						<p class="alignments">The SC 700 is a powerful vacuum oven designed for the reliable and gentle removal of thermoplastic contamination from delicate metallic parts such as filtration discs or extruder components.<br>No cleaning agents required. All contaminants are baked off under vacuum without combustion. This protects the components against local overheating and thus against annealing and mechanical damage.<br>The SC 700 can be installed in any room with a water supply, water drain and an electrical power supply.</p>

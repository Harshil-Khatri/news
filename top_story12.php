					<h1 style="font-size:28px;padding-bottom:35px;">Addex to Introduce Intensive Cooling “Height-Adjustable” Twin-Stack for Blown Film at NPE2018</h1>
					<img src="images\top_story12.jpg" style="float:right;padding: 0px 0px 0px 20px" >
					<div style="font-size:18px;">
 						<p class="alignments">
							Addex Inc., a leading global supplier of auto-profile and other related cooling systems for blown film lines, will showcase its latest Intensive Cooling technology at NPE2018 at the Orlando County Convention Center. The company will launch its “Height-Adjustable” Intensive Cooling Twin-Stack system which is designed to optimize performance for both high- and low-melt processes.
						</p>
 						<p class="alignments">
							Addex offers an enclosed, two-level, stacked, Intensive Cooling system featuring a lower Intensive Cooling element that mounts flat to the die and a second, height-adjustable Intensive Cooling element just below the air ring. The system is highly adaptable to changes in materials and supports fast changeovers.
						</p>
 						<p class="alignments">
							The fully-enclosed Intensive Cooling Twin-Stack elements can be separated over a range of 1-in to 16-in, allowing the operator to change the height between the cooling elements to control the amount of cooling. For super low-melt materials, a short distance between the elements is ideal. For high-melt strength operation, the cooling zone can be extended to its maximum by the push of a button.  The Height-Adjustable Twin-Stack system can be fine-tuned across a broad range of products without time-consuming equipment changes and while the line is running.
						</p>
 						<p class="alignments">
							The original fixed-height Twin-Stack system is proven in the field to increase output by 15% to 20% for very low-melt strength materials, and up to 40% or even 50% for high-melt strength materials. The Height-Adjustable version of the Twin-Stack is expected to produce even greater increases.  The system operates over a wide range of configurations and materials, while also enhancing bubble stability. These retrofits are aimed at blown film lines that have been designed for higher outputs and have sufficient extruder and blower capacity to handle the output gains of Intensive Cooling Twin-Stack. With the addition of Addex’s automatic external gauge control (EGC), customers are generating high-quality film at unprecedented output rates. The Height-Adjustable system is fully compatible with Addex’s other cooling products including the Digital Internal Bubble Cooling Control, Manual Gauge Control, and Laminar Air Rings.
						</p>
 						<p class="alignments">
							At NPE, Addex will also highlight its Intensive Cooling Dual-Flow Air Rings, available for retrofit since 2016. These replace the standard low-velocity lower lip with a single Intensive Cooling element, delivering a guaranteed 10%-15% greater output and better bubble stability, with some processors reporting up to 40% gains in output rate versus the competition.
						</p>
 						<p class="alignments">
							Addex reports strong demand for these super-cooling air rings from processors such as PolyExpert Inc., Laval, Quebec, a leading Canadian producer of polyethylene (PE) film. PolyExpert has purchased several single-element Intensive Cooling air ring systems which have significantly improved output. The air rings are installed with Addex’s auto-profile system which features the most control zones in the industry, reducing gauge variation to well below 5%, 3 sigma. Customers report quick changeovers, solid bubble stability, and increased output rate, which consistently surpasses the Addex guarantee of 10%-15%.
						</p>
 						<p class="alignments">
							“Intensive Cooling products will continue to evolve, fueled by customer feedback and the learning curve, as more systems are put into operation on a variety of processes and materials in production,” said Bob Cree, President of Addex.<br>Today, Addex supplies some of the most sophisticated technologies
						</p>

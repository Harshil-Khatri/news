					<h1 style="font-size:28px;padding-bottom:35px;">The FRA robot comes fully equipped to face today’s plastic industry challenges</h1>
					<div style="font-size:18px;">
						<img src="images\top_story3.png" style="float:right;padding: 0px 0px 0px 20px" >

						<p class="alignments">
							Yushin is proud to announce the release of a new FRA series robot. The new FRA take-out robot incorporates the most innovative design technologies, making it more agile, smarter and faster than previous models. These attributes address the needs of the world’s top plastic injection molding companies who are tasked with producing high quality parts with high speed precision.
						</p>
						<p class="alignments">
							The FRA is equipped with significant patent pending state of the art features including: Active Vibration Control, Arc Motion Control, and Adaptive Motion Control. These features assist in making the FRA achieve cycle times up to 10% faster than its predecessors. The FRA also comes standard with industry leading safety standards, meeting all international safety standards.
						</p>
						<p class="alignments">
							The FRA robot is equipped with the brand new E-touch V controller. The E-Touch V is ergonomically compact and the unique shape fits comfortably in the user’s hand with new tap and swipe features much like smart phones and tablets. The FRA also include dual screen convenience which simultaneous splits screen layout improving robot teaching and eliminating the need to switch between screens. Operators can freely assign I/O signals or buttons of their choice to the sub screen. Grouping frequently used functions together in one screen greatly raises productivity.
						</p>
						<p class="alignments">
							New robot language is included as standard along with the usual familiar Lead Through Teaching and Flexible Teaching programming. All the ease of use and programming safety Yushin users have come to appreciate - now with familiar robot language,  reducing the learning curve and increasing programming versatility for any user.
						</p>
						<p class="alignments">
							Yushin understands the importance of providing our customers with the versatile equipment needed to mold high quality value-added products. Designing the robots with the ability to perform many operations beyond just pick and place makes integration with downstream equipment an easy task. The FRA comes equipped with DeviceNet, EtherCAT, and EtherNet/IP to interface and control all downstream equipment. The FRA accommodates all common post-molded processes like inspection, assembly, and packaging to help users with their press side, value-added manufacturing. The FRA also has PNP/NPN switching capabilities.
						</p>
						<p class="alignments">
							Yushin will be showcasing two FRA models at the NPE show in booth #W2173 located at the West Hall of the Orange County Convention Center.
						</p>
						<p class="alignments">
							The FRA model will be showcased throughout NPE2018 with partnering OEM booths.
						</p>
						<ol class="squares" style="list-style-type: square;background-color:#F5F5F5;padding-left:10px">
							<li class="links" style="width:100%"> 
								Visit <b>Maruka USA</b> in booth <b>#W911</b> where a FRA model will be working on a Toyo Si110-6 press and molding a 4-cavity Silicone Magnifying Lens.
							</li>
							<li class="links" style="width:100%"> 
								Check out <b>Nissei America</b> in booth <b>#W923</b> exhibiting a FRA model on a FNX220IV press, molding a 1 cavity tray.

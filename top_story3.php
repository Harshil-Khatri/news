							<li class="links" style="width:100%"> 
								The FRA robot will also be at <b>Toshiba</b>’s booth <b>#W1563</b> on a TiA250S press, molding a 2 cavity pill box container.<br></br>
							</li>
						</ol>
						<p class="alignments">
							Yushin has also partnered with many OEM booths around the NPE 2018 show. Visit our booth to watch and learn more about our product releases and offerings at NPE 2018.
						</p>
						<p class="alignments">
							Yushin America, Inc., headquartered in Rhode Island, is known worldwide for their expertise in robotics for the plastics industry. They offer a wide range of automation technology, ranging from individual robots for parts removal, to fully integrated factory automation for assembly, decoration, inspection and packaging.   They also provide end-of-arm tool design, conveyors, service and training.  Yushin America, Inc. is the largest subsidiary company of Yushin Precision Equipment Co. Ltd of Japan, one of the world's largest suppliers of robots for the injection molding industry.

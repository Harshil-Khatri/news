					<h1 style="font-size:28px;padding-bottom:35px;">Zeiger to Mold an Automotive LED Headlamp Optic at NPE2018</h1>
					<div style="font-size:18px;">
						<img src="images\top_story4.png" style="float:right;padding: 0px 0px 0px 20px" >

						<p class="alignments">
							At NPE2018, Zeiger Industries (Booth W4143) will be molding an automotive LED headlamp optic made from Dow’s DOWSIL MS-4007 Moldable Silicone on a Milacron – FANUC Roboshot a-S55iA with their new iHMI Controller running an M.R. Mold 2-cavity LSR mold with a single-drop cold runner system. The production cell will utilize an ELMET TOP 5000P dosing system; a Thermal Care air cooled EQ3A01 portable chiller; and a Yushin YC-100S-11-8 robot with an E-Touch Compact controller, a custom engineered end-of-arm tool, and a Yushin indexing conveyor. A virtual molding simulation of the filling and curing process will be provided by SIGMASOFT.
						</p>
						<p class="alignments">
							Zeiger Industries’ LSR Plug-n-Play Conversion Kit is an economical way for thermoplastic molders who have excess capacity to either enter the LSR molding field or increase their LSR machine capacity by simply switching out their existing thermoplastic plasticizing unit with the Zeiger LSR Plug-n-Play Conversion Kit.<br>
							Zeiger’s Plug-n-Play Conversion Kit incorporates a Wexco 777 water-cooled barrel, Zpringlok LSR pre-closed check ring valve, ZSAR PM zero compression ratio LSR screw and their proprietary barrel/screw sealing device.
						</p>
						<p class="alignments">
							The DOWSIL MS-4007 material, like the rest of DOWSIL Moldable Optical Silicone portfolio, exhibits excellent performance in demanding environmental conditions and enables complex optic design that cannot be replicated with more rigid materials.
						</p>
						<p class="alignments">
							<b>About Zeiger Industries</b>
						</p>
						<p class="alignments">
							Zeiger Industries was established in 1990 because of a growing need for a better performing screw tip assembly. Owner Don Zeiger used his 30+ years of processing experience combined with his frustration with poorly designed products made from low cost materials. This led him to design the original Mallard 4-piece screw tip assembly, which evolved to become the highest performing screw tip the world has ever seen. This success lead Zeiger Industries to develop a better way of manufacturing through hardened tool steel screws as well. From building machines from the ground up, to developing our own computerized design and manufacturing systems, Zeiger will never settle for good enough. Zeiger Industries believes in manufacturing as many of its parts in house which allows them to maintain the utmost in quality control.
						</p>
						<p class="alignments">
							<b>About Dow Performance Silicones</b>
						</p>
						<p class="alignments">
							Dow Performance Silicones, a business unit of the Dow Consumer Solutions Portfolio aligned to DowDuPont Materials Science, delivers a complete portfolio of silicone solutions and performance materials to serve the diverse needs of customers and industries around the world. From the transportation and pressure sensitive industries to high performance building solutions and chemical manufacturing, Dow’s Performance Silicones business helps our customers solve their most challenging problems. As a global leader in innovation and silicon-based technology, we are committed to bringing new solutions to the market that do more for our customers and continue to improve the lives of consumers worldwide.
						</p>
						<p class="alignments">
							R.D. Abbott Company, Inc. (RDAbbott) is a full-service supplier that’s been serving the rubber industry since 1948. We are staffed with rubber industry experts, equipped with comprehensive testing laboratories, and stocked with leading elastomer products in warehouse locations throughout North America. Our featured laboratory services include in-house design and development capabilities (under ISO 9001:2015) with a technical team that specializes in formulation development and optimization, material testing, process engineering assistance, product characterization, and innovation development. Our product line encompasses a best-in-class list of elastomeric polymers, rubber chemicals, fillers, bonding agents, and testing equipment—all from respected manufacturers with trusted name brands.
						</p>
						<p class="alignments">
							<b>About ELMET</b>
						</p>
						<p class="alignments">
							Die ELMET Elastomere Produktions of Oftering, Austria, established in 1996, is an internationally successful developer and manufacturer of LSR dosing systems, fully automatic injection molds, valve gate cold decks, and turnkey installations for processing silicones and rubbers. Including the staff of its ELMET North America sales and service organization, the company has some 150 staff.
						</p>
						<p class="alignments">
							<b>About Milacron</b>
						</p>
						<p class="alignments">
							Milacron is a global leader in the manufacture, distribution, and service of highly engineered and customized systems within the plastic technology and processing industries. Milacron is the only global company with a full-line product portfolio that includes hot runner systems, injection molding, blow molding and extrusion equipment, mold components, industrial supplies—plus a wide market range of advanced fluid technologies.
						</p>
						<p class="alignments">
							<b>About M.R. Mold & Engineering</b>
						</p>
						<p class="alignments">
							M.R. Mold & Engineering is a global leader in the design and manufacture of injection molds for LSR, silicone gumstock, thermoset and thermoplastics. From concept through completion, M.R. Mold applies the expertise and technology to speed your product to market. Product development, design, engineering, manufacturing, validation and molding are all performed in-house.
						</p>
						<p class="alignments">
							<b>About Yushin America, Inc.</b>
						</p>
						<p class="alignments">
							Yushin America, Inc. (YAI) was founded in 1988 and is headquartered in Cranston, Rhode Island. They are known worldwide for their expertise in robotics for the plastics industry. They offer a wide range of automation technology, ranging from individual robots for parts removal, to fully integrated factory automation for flexing/closing, assembly, decoration, inspection and packaging. End-of-arm tools, conveyors and safety guarding are also provided by YAI. Their ongoing commitment is to provide customers with innovative automation solutions that maximize savings, minimize risks, and are thoroughly supported over the life of the investment. They are the oldest and largest subsidiary company of Yushin Precision
						</p>
						<p class="alignments">
							<b>About Thermal Care</b>
						</p>
						<p class="alignments">
							Since 1969, Thermal Care has manufactured water chillers and process cooling equipment for industry worldwide. Skillfully engineered temperature controllers, portable chillers and central chillers, cooling towers and pump/reservoirs, provide Thermal Care customers with a wide range of process cooling solutions from off-the-shelf models to integrated, customized systems.
						</p>

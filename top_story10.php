					<h1 style="font-size:28px;padding-bottom:35px;">Vitamix “Rethinks the Possible” with Carbon’s  Complete 3D Manufacturing Solution</h1>
					<img src="images\top_story10.png" style="float:right;padding: 0px 0px 0px 20px" >
					<div style="font-size:18px;">
						<p class="alignments">
							<b>REDWOOD CITY, CA</b> – Carbon , a Silicon Valley-based digital 3D Manufacturing company, today announced its innovative partnership with Vitamix, a global leader in high-performance blending equipment for home and commercial use. Together with The Technology House (TTH), a Carbon Production Partner and leading contract manufacturer based in Ohio, the companies reimagined part design and production to create an innovative micro-fluidic nozzle at mass scale that is 10 times more durable, uses 30 percent less material, and is 33 percent more economica
						</p>
						<p class="alignments">
							“If you're going to believe in something that isn't yet possible, then you have to rethink possible. It's the only way to get there,” said Jodi Berg, President and CEO, Vitamix. “Our partnership with Carbon is very exciting for us, because it's helping us rethink the possible every day. Once you realize you can manufacture something differently, then you can think about how those components interact with other components, and how you can achieve an entirely different outcome. It's about evolving the paradigm of engineering, and now we have a new tool in our toolbox that we're learning, exploring, and evolving.”
						</p>
						<p class="alignments">
							With a shared commitment to pushing the boundaries of industrial design, Carbon, Vitamix and TTH joined forces to reimagine how they could design, engineer, make and deliver the Vitamix blender’s pressurized nozzle – a critical part of a system used to rinse and clean the containers in commercial shops all over the country. Historically, the nozzle was designed to be six different injection-molded pieces, and needed to be made with durable materials that can withstand high pressures and temperatures, as well as regular exposure to strong cleaning agents like bleach, detergents, and sanitizers.
						</p>
						<p class="alignments">
							Seeking a simpler and more economical design, Vitamix turned to TTH, and they set their sights on the ambitious goal of manufacturing the six-part nozzle as a single, monolithic part. Carbon’s complete 3D Manufacturing solution enabled them to make the unmakeable; using its durable Rigid Polyurethane (RPU) material, SpeedCellä system (which includes M2 printers and Smart Part Washer), and proprietary Digital Light Synthesisä technology, they were able to manufacture complex geometries and channels with excellent surface finish, a critical element due to the high-pressure fluidics performance requirement of the nozzle. As a result, the Carbon-produced part exceeded the quality standards previously achieved through traditional manufacturing, and Vitamix is deploying tens of thousands of these nozzles into stores all over the country.
						</p>
						<p class="alignments">
							“There is a liberating freedom when designing products on the means of production, and our work with Vitamix is a perfect example of the kind of innovation you can achieve when you fuse design, manufacturability and engineering all into one,” said Dr. Joseph DeSimone, CEO and Co-founder at Carbon. “Once you go digital, you can't go back – it fundamentally changes how designers and engineers think, how people collaborate and work, without constraints. That’s true 3D Manufacturing, and it's revolutionary because it allows for powerful, imaginative thinking to solve complex problems and create new opportunities.
						</p>
						<p class="alignments">
							<b>About Carbon</b>
						</p>
						<p class="alignments">
							Carbon is a Silicon Valley-based company working at the intersection of hardware, software, and molecular science to deliver on the promise of digital 3D Manufacturing. The company empowers its customers and partners to evolve beyond using 3D printing for basic prototyping to producing at scale by revolutionizing how they design, engineer, make, and deliver their products. With Carbon’s Digital Light Synthesis technology and its SpeedCell system (M Series printers and Smart Part Washer), manufacturers can unlock new business opportunities such as mass customization, on-demand inventory, and differentiated products made with unique functional materials. Carbon's solutions also provide substantial operational efficiencies, such as the elimination of tooling costs and decreased time to market.
						</p>

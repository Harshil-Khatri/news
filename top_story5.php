					<h1 style="font-size:28px;padding-bottom:35px;">Dynamic Conveyor Presents the Only Conveyor that can be Reconfigured at NPE ‘18 Adaptable DynaCon Conveyor Systems Can Last a Lifetime</h1>
					<div style="font-size:18px;">
						<img src="images\top_story5.png" style="float:right;padding: 0px 0px 0px 20px" >
						<p class="alignments">
							Dynamic Conveyor will be presenting a variety of DynaCon Conveyor Systems to the plastics industry at NPE ’18 in Orlando, Florida. DynaCon conveyor systems offer customization using standard components and a variety of accessory options to create the ideal conveying system. Reconfigurability and the ease of adding accessories offers systems that are so flexible that purchasing replacement conveyors may never be necessary again.
						</p>
						<p class="alignments">
							Dynamic Conveyor staff will be available at Booth #W-4373 to answer questions about new products and enhancements along with how DynaCon conveyor systems can bring a quick return-on-investment to the plastics industry.
						</p>
						<p class="alignments">
							Dynamic Conveyor now offers both In-Line and Side-by-Side Box Filling Systems by part count, cycle count or weight. All Box Filling Systems are engineered to order to meet your specific box filling needs. Systems allow user defined fill rates to insure accuracy up to 0.3 ounces, providing hours of unattended precise container filling. Offering a smooth transfer of boxes from 0 – 200 lbs., the systems are automated and allow for unattended operation of filling with simple control set-up and an easy to read visual indicator.
						</p>
						<p class="alignments">
							DynaCon Tumbler Separators have been redesigned to offer superior durability and easy adjustability. With two rotating cage options, DynaCon Tumbler Separators offer increased parts and runner separation accuracy over standard drum separators. Cages with adjustable fins can be easily moved and adjusted to meet separation needs. Cages with interchangeable sleeves, with custom holes, allow for optimal parts and runner separation. The sleeves can be easily switched in and out in less than 60 seconds to meet a variety of parts separation needs.
						</p>
						<p class="alignments">
							The Vertical Z Conveyor saves floor space when there is a need to get parts to a higher elevation. The DynaCon vertical incline conveyor is custom designed to your desired height and includes widths ranging from 4” to 60”. The conveyor can include an optional hopper designed to meet desired parts conveying needs. Optional casters are available to make the system easy to move in and away from other equipment. Radius Turn Conveyor Systems feature plastic sides to make the required lateral turns (from 5<sup>o</sup> to 180<sup>o</sup>). Radius turn modules are easily connected to standard conveyor modules, including incline/decline modules, to build one conveyor with continuous belt flow. Radius Turn Conveyors offer money savings by using one drive motor on long running conveyors with one or numerous opposing turns.
						</p>
						<p class="alignments">
							A large variety of accessories are compatible with DynaCon conveyors including those that will be on display: Metal detection, fan cooling, small product transfer and a product diverting arm.
						</p>

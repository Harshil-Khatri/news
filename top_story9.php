						<p class="alignments">
							By working with customers from initial product design stage, CN team has realized the overmolding thermoplastic onto PCB with battery contained. While maintaining the PCB’s function, the cosmetics of the product meets up with modern consumer products’ standard. By this, wearable products can be designed to be more integrated - lighter and stronger- as well as saving assembly labor cost.
						</p>
						<p class="alignments">
							“The integrity of modern consumer products are greatly credited to the advancement of insert molding and overmolding”, said Peter Huang, the founder of CN Mould & Plastic, a mechanical engineer who has worked in mold making industry over 30 years, “So, the next thing we are facing is to make more inserts ‘moldable’ ”.
						</p>
						<p class="alignments">
							<b>About CN MOULD & PLASTIC: The Injection Mold Maker and Molder</b>
						</p>
						<p class="alignments">
							Founded in 2000, CN Mould & Plastic Limited specializes in insert molding and overmolding and have produced over hundreds type of overmolding parts thus far. Our parts are being supplied to world-famous customers including HP, DELL, Lenovo, Harman, Skull-Candy and their completeness represent the frontiers of overmolding technology.
						</p>
						<p class="alignments">
							By assisting customers from design development and prototyping stage to mass production, the company expands gradually in the past decade and now owns over 100 injection molding machines – press sizes range from 18 Tons to 1200 Tons – as well as low-pressure molding,silicone (Liquid Silicone Rubber) molding, die cut and foam splitting capabilities.
						</p>
						<p class="alignments">
							<b>About NPE2018: The Plastics Show</b>
						</p>
						<p class="alignments">
							Recognized as the world’s largest plastics trade show and conference, NPE2018 brings together all industry sectors, including vertical and peripheral markets, end markets, brand owners and recyclers. Held every three years, NPE attracts more than 65,000 plastics professionals and features industry-focused education and leading suppliers of emerging technologies, equipment and materials for every phase of plastics processing, all in 1 million square feet of exhibit space.
						</p>
						<p class="alignments">
							Building on its most successful show in 2015, held in Orlando, Florida, NPE2018 anticipates even greater participation from up and down the global plastics supply chain when it returns to Orlando in 2018. NPE is produced by Plastics Industry Association (PLASTICS) and is the only international plastics event produced by the industry for the industry.
						</p>

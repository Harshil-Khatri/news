					<h1 style="font-size:28px;padding-bottom:35px;">New iQ Auto-Plus Ultrasonic Generator with Integrated Multi Probe Controller</h1>
					<div style="font-size:18px;">
						<img src="images\top_story6.png" style="float:right;padding: 0px 0px 0px 20px" >
						<p class="alignments">
							Dukane, St. Charles, IL. introduces <b><i>iQ</i> Auto-Plus Ultrasonic Generator with patented Multi Probe Control (MPC)</b> module integrated in same unit. The generator is designed for automated machines that require ultrasonic staking or spot welding. Perfectly suited for automotive interior door panels and other large ultrasonic multi-point applications. The compact size of the new integrated unit allows for panel mounting, either internal or external, to an electrical cabinet.  This configuration is well suited for cassette-style automated machines as it drastically reduces cabling requirements.
						</p>
						<p class="alignments">
							The standard unit comes with EtherNet/IP or Modbus and has optional Anybus Module (Profinet, Profibus, Powerlink, EtherCAT, CC-Link) communication protocols.  All these protocols allow for faster interaction with the automated system. Unit’s front panel has discrete multi-color status indicators for each weld point and overall system faults, which makes system setup and troubleshooting quick and efficient.
						</p>
						<p class="alignments">
							<i>iQ</i> Auto+MPC can be configured to have 8 individual weld points, each supplied standard with Weld by Time, Energy or Peak Power with optional I/O for Weld by Distance, coupled with Dukane’ s patented Trigger by Power to provide unprecedented weld consistency. All these control features are processed inside the generator utilizing .5 millisecond processing rate, which is significantly faster than competitive systems or systems that rely on a PLC for the processing.
						</p>
						<p class="alignments">
							Designed to meet a wide range of application requirements, the <i>iQ</i> system is available in two power ranges 600 or 1200 watt and four frequencies 20, 30, 35 or 40 kHz. To simplify and reduce power requirements, it has universal voltage input 110 or 240 VAC and incorporates Dukane’ s new PFC highly-efficient (>92%) power conversion.  Dukane’s patented MPC protection circuitry prevents this unit to initiate ultrasonic power when residual current is present.  This drastically reduces ultrasonic probe failures and machine downtime.
						</p>

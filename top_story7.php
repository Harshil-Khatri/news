					<h1 style="font-size:28px;padding-bottom:35px;">Conair Advances the Science of Material Handling  With Enhanced Wave Conveying System</h1>
					<div style="font-size:18px;">
						<img src="images\top_story7.png" style="float:right;padding: 0px 0px 0px 20px" >
						<p class="alignments">
							New developments in the operation and application of The Conair Wave Conveying material-handling system now give processors a wide range of options to achieve an unprecedented level of control over the vacuum conveying process. The patented system now makes it possible to move any resin, at virtually any speed, with higher throughputs, over longer distances without the damage to materials and equipment normally associated with conventional, dilute-phase vacuum conveying.
						</p>
						<p class="alignments">
							“We originally introduced this technology at NPE 2015 with a focus on slow-speed, dense-phase conveying in order to prevent problems like angel hair and pellet fracturing,” explains Chad Stover, Conair Marketing Communications Manager. “Since then, we have refined the concept, which we now call Wave Conveying, to give processors the freedom to move different materials at the ideal speed for each application. The fact that the system can change speeds automatically when different resins are called for is very exciting.”
						</p>
						<p class="alignments">
							The Conair Wave Conveying system is the first vacuum-powered material-handling system to give processors precise control of material speed and material flow. It does this through the interaction of Conair’s FLX-128 Plus conveying control, a Conair Wave Conveying vacuum pump with a variable-frequency drive, a Conair Wave Conveying control valve, with standard receivers and tubing.  Starting at speeds of about 300 fpm, the Wave Conveying system begins to draw material through in the form of compact pulses or waves, each separated by an air space. As velocity increases, the interval between the pulses becomes shorter, and the waves flow faster. Pellets do not become suspended in conveying air as they do in conventional high-speed systems, but they look like waves as pellets roll and tumble along the bottom of the conveying line.
						</p>
						<p class="alignments">
							Using the patented Wave Conveying system, processors can achieve very high resin throughputs, typically higher than those possible with high-speed dilute-phase conveying systems. That’s because the resin flowing in waves, though moving slower, is far more concentrated and densely packed in the cross-section of the tube than a dilute-phase material stream, where comparatively few pellets are present in a large volume of air.
						</p>
						<p class="alignments">
							The speed at which pellets travel is important. Since central vacuum-conveying systems were first introduced decades ago, processors have had to accept certain problems related to high-speed conveying. Conventional dilute-phase systems pick up resin pellets in a high-speed jets of air and move them at velocities of 4500-6000 fpm. As pellets shoot through these systems, speed and friction cause damage. Soft pellets are heated by friction as they scrape against tube walls, leaving behind smears and streamers—snake skins and angel hair. Harder pellets that collide with hard surfaces—or other pellets—can degrade, generating fines and dust. And the abrasive glass- and mineral-filled resins can literally scrape away the walls of conveying tubing, causing leaks. “Angel-hair traps and specially designed elbows can be used to deal with the adverse effects of high-speed conveying,” says Doug Brewster, Conair Conveying Product Manager, “but only Wave Conveying technology can actually prevent them.”
						</p>
						<p class="alignments">
							<b> Wave conveying is tunable and energy-efficient</b>
						</p>
						<p class="alignments">
							Harnessing the resin-protection, long-distance attributes, and throughput benefits of Wave Conveying demands more precise and predictable control of conveying equipment, air and material speeds, and material flow patterns than ever before. The key to the ability of Wave Conveying technology to regulate the conveying process – even to allow for programmable resin-conveying ”recipes” to be stored in the FLX-128 Plus – is found in a series of patented control and equipment innovations.  Here are a few examples:
						</p>
						<ol class="squares" style="list-style-type: square;background-color:#F5F5F5;padding-left:10px">
							<li class="links" style="width:100%"> 
								The Wave Conveying system uses a Conair LDP Series vacuum pumps equipped with variable frequency drives. Unlike typical vacuum pumps that are 100% on or off, LDP pumps with variable-speed drives can operate at the precise level of capacity and power consumption needed to maintain a specific material velocity in the system.  Depending on system usage, this can result in substantial energy savings.
							</li>
							<li class="links" style="width:100%"> 
								The Wave Conveying hardware, together with the FLX-128 Plus control regulates material flow, fine tuning both the vacuum level and the influx of material from the material supply point to the conveying system.
							</li>
							<li class="links" style="width:100%"> 
								A new type of conveying speed sensor, a sensor that measures material velocity, not air velocity, was another system first. This sensor monitors the speed of material moving in the mixed-pulse and wave phases that are characteristic of Wave Conveying. It provides feedback to the FLX control and Wave Conveying system, helping the system maintain even and correct material flows throughout.<br></br>
							</li>
						</ol>
